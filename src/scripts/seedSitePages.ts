async function seedSitePages() {
  await connectMongo();

  const entries = Object.entries(sitePageSeeds);
  for (const [pageId, sections] of entries) {
    await SitePage.findOneAndUpdate(
      { pageId },
      { pageId, sections },
      { upsert: true, new: true, runValidators: true },
    );
    console.log(`Seeded: ${pageId}`);
  }

  console.log(`Done. Seeded ${entries.length} site pages.`);
}

seedSitePages()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error("Failed to seed site pages:", error);
    process.exit(1);
  });
